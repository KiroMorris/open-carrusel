#!/usr/bin/env node
/**
 * Build the editor runtime into an injectable JS string.
 *
 * Why this exists:
 *   `src/lib/editor-runtime.ts` runs INSIDE the editor iframe, not under
 *   Next.js's bundler. We need its compiled JS body as a literal string at
 *   render time so `wrapSlideHtml()` can drop it into a <script> tag. This
 *   script reads the .ts source, strips TypeScript syntax using Node 22's
 *   built-in `module.stripTypeScriptTypes()` (no extra dep), and writes a
 *   `editor-runtime.bundle.ts` module that exports the JS as a const.
 *
 * Approach (a) chosen over (b)/(c) (per Phase 2 plan):
 *   - (a) [chosen] Use Node 22's built-in `stripTypeScriptTypes`. Zero new
 *     deps. Same stripper Node already uses for `.ts` files under the
 *     experimental loader, so the supported subset is well-defined and
 *     matches what the rest of the project already relies on (the test
 *     loader uses `--experimental-strip-types` too).
 *   - (b) Hand-rolled regex stripper — rejected. Brittle around object
 *     literals (`{ key: value }` looks like a type annotation); we burned
 *     time on this before discovering Node had a built-in.
 *   - (c) Pull in esbuild/swc — rejected. They're not already in
 *     node_modules and the runtime is small enough that a 200KB dep would
 *     be silly.
 *
 * Run: `npm run build:editor-runtime`
 * The output (`src/lib/editor-runtime.bundle.ts`) IS committed so the dev
 * server picks up changes without an explicit build step.
 */

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";
import { stripTypeScriptTypes } from "node:module";

// Silence Node's ExperimentalWarning for stripTypeScriptTypes — the API is
// already in widespread use across this project (the test loader assumes it).
process.removeAllListeners("warning");
process.on("warning", (w) => {
  if (w.name === "ExperimentalWarning" && /stripTypeScriptTypes/.test(w.message)) return;
  console.warn(w);
});

const HERE = dirname(fileURLToPath(import.meta.url));
const SRC = resolve(HERE, "..", "src", "lib", "editor-runtime.ts");
const OUT = resolve(HERE, "..", "src", "lib", "editor-runtime.bundle.ts");

const tsSource = readFileSync(SRC, "utf8");

// `stripTypeScriptTypes` returns the type-stripped JS, preserving line
// numbers (replacing types with whitespace). We don't bother minifying —
// the runtime is small and the parent gzip-compresses anyway. Comments are
// preserved by default; we strip the big block comment header to keep the
// injected payload tight.
let jsCode = stripTypeScriptTypes(tsSource, { mode: "strip" });

// Drop the leading top-of-file block comment header (everything before the
// first `function` declaration) — it's developer documentation and adds
// ~1KB to every iframe payload for no runtime benefit.
const firstFn = jsCode.search(/^[a-z]/m);
if (firstFn > 0) {
  jsCode = jsCode.slice(firstFn);
}

// Smoke-test that the output is valid JS so a stripper regression doesn't
// silently bake broken JS into every iframe.
try {
  // eslint-disable-next-line no-new-func
  new Function(jsCode);
} catch (err) {
  console.error("\n[build-editor-runtime] STRIPPED OUTPUT IS NOT VALID JS:");
  console.error(err.message);
  console.error("\n--- first 80 lines of output ---");
  jsCode
    .split("\n")
    .slice(0, 80)
    .forEach((l, i) => console.error(`${String(i + 1).padStart(3)}: ${l}`));
  process.exit(1);
}

const banner = `/**
 * AUTO-GENERATED by scripts/build-editor-runtime.mjs from src/lib/editor-runtime.ts.
 * DO NOT EDIT BY HAND. Run \`npm run build:editor-runtime\` to regenerate.
 */
/* eslint-disable */
`;

const out = `${banner}export const EDITOR_RUNTIME_JS = ${JSON.stringify(jsCode)};\n`;

writeFileSync(OUT, out, "utf8");

console.log(
  `[build-editor-runtime] wrote ${OUT} (${jsCode.length} chars JS, ${out.length} chars output)`
);
